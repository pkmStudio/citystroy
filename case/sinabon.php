					<h1>Инженерные системы</h1>
					<h2>Для подачи воды в первый колодец установили глубинный насос и установили систему очистки воды, такая вода используется только для технических нужд. Для питьевой воды внутри помещения поставили специальные маленькие насосы для подкачки питьевой воды из бутылей по 20л. Вода из колодца для питьевых нужд не используется. Канализацию подключили к предназначенному для этого второму колодцу. Так как объект представляет собой отдельно стоящее здание и по периметру имеет сплошное остекление, следовательно, существует проблема больших теплопотерь в холодное время. Как наиболее оптимальное решение, отопление ресторана мы выполнили электрическими радиаторами по периметру, а также теплыми полами в гостевой зоне и в зоне работы персонала. Над дверьми установили тепловые завесы, препятствующие быстрому оттоку тепла из помещения при открытых дверях. Для охлаждения мы использовали кондиционер канального типа, разместив внутренний блок в технической части, от которого расходятся трассы воздуховодов до стен, а в самих стенах установлены горизонтальные решетки. Таким образом мы ушли от громоздкого настенного блока в зале, который мог испортить интерьер. Внешний блок был установлен на крыше на уже существующую раму. Приточно-вытяжную вентиляцию мы собирали с нуля — воздуховоды проложили под потолком. А для того, чтобы они не занимали много места и в целях экономии объема помещения, сделали вместо одного воздуховода, вереницу воздуховодов небольшого диаметра. Систему охлаждения и приточно-вытяжную установку, которая зимой подогревает приточный воздух, мы смонтировали на крыше. Так как всё охлаждение и отопление работает на электрической мощности, у нас получился достаточно сложный электрический распределительный щит.</h2>
