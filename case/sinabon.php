					<h1>Технические условия</h1>
					<h2>Само помещение — это отдельно стоящее здание на центральной площади многофункционального комплекса Солнце Москвы, расположенное рядом с колесом обозрения. На момент начала работ здание представляло собой 8 несущих стальных металлических круглых колонн, которые держат плиту перекрытия, а вокруг них установлено панорамное остекление по всему периметру. Высота до перекрытия 3м. Горячего холодного водоснабжения, водяного отопления, охлаждения, канализации изначально не было. 
						В наличии были два колодца, рядом со зданием, закопанных в землю, один из которых предусмотрен для технической воды, а другой для отработанных стоков. Приточно-вытяжной вентиляции в помещении не было, только отверстие в перекрытии. Так как обогреваться и охлаждаться надо было самостоятельно, то электрической мощности было выделено столько, сколько выделяют на полноценный ресторан, то есть «за глаза». Следует отметить, что черновой пол был довольно сильно заглублен относительно нулевой отметки, что позволило легко развести все инженерные коммуникации в полу.</h2>
